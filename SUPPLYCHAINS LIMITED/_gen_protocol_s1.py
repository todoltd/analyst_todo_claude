def esc(s):
    return s.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;")

# ---- run builders (mirror reference styling) ----
RFONTS = '<w:rFonts w:ascii="Times New Roman" w:cs="Times New Roman" w:eastAsia="Times New Roman" w:hAnsi="Times New Roman"/>'

def run_plain(text):
    return '<w:r><w:t xml:space="preserve">%s</w:t></w:r>' % esc(text)

def run_bold(text):
    return '<w:r><w:rPr><w:b/></w:rPr><w:t xml:space="preserve">%s</w:t></w:r>' % esc(text)

# results paragraph wrapper (firstLine 142, italic in pPr rPr like the reference)
def res_para(runs_xml):
    return ('<w:p w:rsidR="00000000" w:rsidDel="00000000" w:rsidP="00000000" w:rsidRDefault="00000000" w:rsidRPr="00000000">'
            '<w:pPr><w:ind w:firstLine="142"/><w:rPr>%s<w:i w:val="1"/><w:iCs w:val="1"/>'
            '<w:sz w:val="24"/><w:szCs w:val="24"/></w:rPr></w:pPr>%s</w:p>') % (RFONTS, runs_xml)

def heading_para(text):
    return res_para(run_bold(text))

def body_para(text):
    return res_para(run_plain(text))

def label_para(label, rest):
    # bold label + space, then normal continuation (may be empty)
    runs = run_bold(label + " ")
    if rest:
        runs += run_plain(rest)
    return res_para(runs)

def bullet_para(text):
    return res_para(run_plain("• " + text))  # "• "

