def fill_run_after_paraid(xml, paraid, text, rpr_inner=None):
    """Find paragraph with given paraId, replace its first empty <w:r>...</w:r>
    (the rtl-only run) with a run carrying text."""
    idx = xml.find('w14:paraId="%s"' % paraid)
    if idx == -1:
        raise RuntimeError("paraId %s not found" % paraid)
    # find the empty run after idx
    m = re.search(r'<w:r>\s*<w:rPr>\s*<w:rtl w:val="0"/>\s*</w:rPr>\s*</w:r>', xml[idx:])
    if not m:
        raise RuntimeError("empty run after %s not found" % paraid)
    start = idx + m.start()
    end = idx + m.end()
    if rpr_inner is None:
        rpr_inner = '<w:rtl w:val="0"/>'
    newrun = '<w:r><w:rPr>%s</w:rPr><w:t xml:space="preserve">%s</w:t></w:r>' % (rpr_inner, esc(text))
    return xml[:start] + newrun + xml[end:]

